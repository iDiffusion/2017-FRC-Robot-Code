package org.usfirst.frc.team5854.Utils;

public class PixelCoord {
	public int firstx, firsty, secondx, secondy;
	
	public PixelCoord(int firstx, int firsty, int secondx, int secondy) {
		this.firstx = firstx;
		this.firsty = firsty;
		this.secondx = secondx;
		this.secondy = secondy;
	}
}
